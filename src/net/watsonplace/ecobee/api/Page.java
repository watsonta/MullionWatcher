package net.watsonplace.ecobee.api;

public class Page {
	private int page;
	private int totalPages;
	private int pageSize;
	private int total;
	
	public int getPage() {
		return page;
	}
	
	public int getTotalPages() {
		return totalPages;
	}
	
	public int getPageSize() {
		return pageSize;
	}
	
	public int getTotal() {
		return total;
	}
}
